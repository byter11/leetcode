class Solution:
    def numOfSubarrays(self, arr: List[int]) -> int:
        ans = 0
        odds = 0
        evens = 0
        s = 0
        
        for i, a in enumerate(arr):
            s += a
            if s % 2 == 0:
                evens += 1
                ans = (ans + odds) % (1e9+7)
            else:
                odds += 1
                ans = (ans + 1 + evens) % (1e9+7)
        
        return int(ans)


